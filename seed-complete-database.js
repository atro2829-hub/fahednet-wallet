async function seedDatabase() {
  console.log('🚀 Starting complete database seed...');
  console.log(`📦 Categories: ${Object.keys(categories).length}`);
  console.log(`📦 Sub-Categories: ${Object.keys(subCategories).length}`);
  console.log(`📦 Providers: ${Object.keys(providers).length}`);
  console.log(`📦 Network Prefixes: ${networkPrefixes.length}`);

  const packages = generatePackages();
  console.log(`📦 Packages: ${Object.keys(packages).length}`);

  try {
    // 1. Write categories
    console.log('\n📝 Writing categories...');
    await set(ref(db, 'categories'), categories);

    // 2. Write sub-categories
    console.log('📝 Writing sub-categories...');
    await set(ref(db, 'subCategories'), subCategories);

    // 3. Write providers
    console.log('📝 Writing providers...');
    await set(ref(db, 'providers'), providers);

    // 4. Write packages
    console.log('📝 Writing packages...');
    await set(ref(db, 'packages'), packages);

    // 5. Write network prefixes
    console.log('📝 Writing network prefixes...');
    const prefixUpdates = {};
    networkPrefixes.forEach((p, i) => {
      const pId = `prefix_${i}`;
      prefixUpdates[pId] = { ...p, id: pId };
    });
    await set(ref(db, 'adminSettings/networkPrefixes'), prefixUpdates);

    // 6. Write owner settings sections
    console.log('📝 Writing owner settings sections...');
    await set(ref(db, 'ownerSettings/sections'), ownerSections);

    // 7. Write visibility settings
    console.log('📝 Writing visibility settings...');
    await update(ref(db, 'adminSettings/visibility'), {
      sections: visibilitySections,
      providers: visibilityProviders,
      subCategories: visibilitySubCategories,
    });

    // 8. Write section visibility (legacy compatibility)
    console.log('📝 Writing legacy visibility...');
    await set(ref(db, 'adminSettings/sectionVisibility'), visibilitySections);

    console.log('\n✅ Database seed completed successfully!');
    console.log('\n📊 Summary:');
    console.log(`   - ${Object.keys(categories).length} categories`);
    console.log(`   - ${Object.keys(subCategories).length} sub-categories`);
    console.log(`   - ${Object.keys(providers).length} providers`);
    console.log(`   - ${Object.keys(packages).length} packages`);
    console.log(`   - ${networkPrefixes.length} network prefixes`);

    // Count entertainment providers
    const entProviders = Object.values(providers).filter(p => p.categoryId === 'entertainment');
    console.log(`   - ${entProviders.length} entertainment providers (200+ target)`);

    process.exit(0);
  } catch (error) {
    console.error('❌ Error seeding database:', error);
    process.exit(1);
  }
}

seedDatabase();
